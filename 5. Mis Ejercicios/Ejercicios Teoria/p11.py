'''
    PROCEDIMIENTOS y FUNCIONES:
    
    Son BLOQUES de CODIGO que permiten ENCAPSULAR/AGRUPAR cierta parte de CODIGO para realizar una TAREA CONCRETA
    SIMEPRE QUE QUERAMOS SIN NECESIDAD DE REPETIR EL CODIGO.
    
    El ESQUEMA de UNA FUNCION EN PYTHON ES:
    
    def <nombre_de_la_funcion>(parametro1, parametro2,..., parametron):
        BLOQUE de INSTRUCCIONES.
        return <resultado>
    
    Una vez DEFINIDA la FUNCION, para EJECUTARLA/LLAMARLA/INVOCARLA:
    
        nombre_de_la_funcion(lista_parametros_de_entrada)
    
    !!!ANTES DE LLAMAR A UNA FUNCIÓN ES OBLIGATORIO HABERLA DEFINIDO¡¡¡
'''
'''
    SENTENCIAS:
    
    Una SENTENCIA es UNIDAD EJECUTABLE mas pequeña de un programa --> En su minima expresión puede verse como UNA INSTRUCCIÓN.
    
    Siempre consiste en realizar UNA o MAS OPERACIONES sobre uno o más DATOS.
    
    Las OPERACIONES se indican por medio de:
        1.- PALABRAS CLAVE del LENGUAJE --> IF, WHILE,...
        2.- OPERADORES --> +, -, =, ==,...
        3.- FUNCIONES --> PROPIAS del LENGUAJE como "print()" o PERSONALIZADAS.
    
    Los DATOS pueden encontrarse en la SENTENCIA:
        1.- de FORMA DIRECTA:
        2.- en "CONTENEDORES" como VARIABLES o CONSTANTES:
    
    TIPOS DE SENTENCIAS:
        1.- ASIGNACION  --> Se ASIGNA un VALOR a una VARIABLE o CONSTANTE.
        2.- DECLARACION --> Se CREA una VARIABLE/CONSTANTE o una FUNCION.
        3.- OPERACION   --> Se MODIFICA un DATO a través de algún tipo de OPERACION.
        4.- SELECCION   --> Se DIVIDE la SENTENCIA en función de una CONDICION LOGICA.
        5.- ITERACION   --> Se REPITEN BLOQUES de CODIGO en función de una CONDICION LOGICA.
'''