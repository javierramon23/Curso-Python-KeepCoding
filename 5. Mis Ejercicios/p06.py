'''
    Algunas OPERACIONES matematicas interesantes en Python:
'''
a = 3
b = 2

# Division Entera:
# Devuelve la parte entera del COCIENTE de una división.
print(a // b)

# Modulo/Resto de la Division:
# Devuelve el RESTO del resultado de una división.
print(a % b)

# Potencia:
print(a ** b)

